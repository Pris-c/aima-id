/**
 * Represents a the address of a user.
 *
 * @property street represents the name of the street of the user.
 * @property number represents the number of the user's residence.
 * @property city represents the city of the user's residence.
 * @property postalCode represents the postal code of the given address.
 */
class Address (
    val street: String,
    val number: Int,
    val city: String,
    val postalCode: Int,
)


